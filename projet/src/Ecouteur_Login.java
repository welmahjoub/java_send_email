
import java.awt.event.ActionEvent;
import java.awt.event.ActionListener;
import javax.swing.JOptionPane;


public class Ecouteur_Login implements ActionListener {
	
    /*========================================================================================================================*/
   	/*========================================= 2) fonction  ================================================*/
   	/*========================================================================================================================*/
	
	public Fenetre_Login fen;
	   private Dao_Login d;
	    /*========================================================================================================================*/
	   	/*========================================= 2) fonction  ================================================*/
	   	/*========================================================================================================================*/
	   
	public Ecouteur_Login(Fenetre_Login fen)
	{
		this.fen=fen;
		d=new Dao_Login();
	}
    /*========================================================================================================================*/
   	/*========================================= 2) fonction  ================================================*/
   	/*========================================================================================================================*/
	
	@Override
	public void actionPerformed(ActionEvent arg0) {
		
		seconnecter();
		
	}
	
    /*========================================================================================================================*/
   	/*========================================= 2) fonction  ================================================*/
   	/*========================================================================================================================*/
	void seconnecter()
	{

		@SuppressWarnings("deprecation")
		boolean test= d.seconnectez(fen.txt_login.getText(), fen.txt_pass.getText());
		
		if(test==true)
		{
			     
			  Fenetre_Principale fe=new Fenetre_Principale();
			 fe.setVisible(true);
		      fen.dispose();
		      
		}
		else
		{
			JOptionPane.showMessageDialog(null, "   Mot  de passe ou username incorrect ");
		}
			
	}
    /*========================================================================================================================*/
   	/*========================================= 2) fonction  ================================================*/
   	/*========================================================================================================================*/

}
